/*最大的困难是读题,罗嗦了一大堆,其实就是一道找对于序列{ak},满足各
元素互不相同,和为n,且该序列各项乘积最大.
容易得出满足条件序列的几个性质:
1.4>a1>1
2.a[i+1]-a[i]<=2(假设该序列按升序排列)
3.a[i+1]-a[2]=2的情况最多有1个
具体证明略.
由此可以轻松构造出该序列:
1.从2开始累加,直到和m大于等于n
2.j=m-n,把序列中的j划去(当j=1时,划去2并将最后一个元素加1)*/
 
//程序如下:
#include <stdio.h>
int main() 
{ 
	int n,i=4,m=5,j; 
	scanf("%d",&n); 
	while (m<n) 
		m+=i++; 
	j=m-n; m=2; 
	if (j==1) { 
		m=3; j=i-1; i++; 
	} 
	while (m<i-1) { 
		if (m!=j) 
			printf("%d ",m); 
		m++; 
	} 
	printf("%d\n",i-1);
	return 0; 
}
